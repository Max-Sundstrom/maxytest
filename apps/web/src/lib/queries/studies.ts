/**
 * Server-side error code matcher. The publish RPC raises a Postgres
 * exception whose `message` carries the literal code we want to surface
 * (`no_question_blocks`, `forbidden`, `study_not_found`). PostgREST forwards
 * the message text into `PostgrestError.message`; we string-match defensively
 * because the precise wire format may include a "ERROR: " prefix or trailing
 * SQLSTATE on some Supabase versions.
 */
function hasErrorCode(err: unknown, code: string): boolean {
  if (!err || typeof err !== 'object') return false;
  const msg = (err as { message?: string }).message;
  return typeof msg === 'string' && msg.includes(code);
}

export interface PublishStudyResult {
  run_token: string;
  status: 'published';
}

/**
 * D-27 publish flow.
 *
 * On success:
 *   - Copies `${origin}/r/{run_token}` to clipboard (best-effort — clipboard
 *     may be denied by browser permissions; the toast still fires).
 *   - Toasts "Test published. Share link copied to clipboard." (UI-SPEC.md
 *     §Status lifecycle copy lock).
 *   - Invalidates list + single-study caches.
 *
 * On `no_question_blocks` server error: throws `ValidationError` so the
 * caller's `onError` can surface the destructive toast "Add at least one
 * question block before publishing." (copy lock).
 */
export function usePublishStudy() {
  const qc = useQueryClient();
  return useMutation({
    mutationFn: async (input: {
      studyId: string;
      workspaceId?: string | null;
    }): Promise<PublishStudyResult> => {
      // `publish_study` was added in migration 00004 and is not yet in
      // types.gen.ts — narrowed via `as never` (regenerated post-merge).
      const { data, error } = await supabase.rpc(
        'publish_study' as never,
        { study_uuid: input.studyId } as never,
      );
      if (error) {
        if (hasErrorCode(error, 'no_question_blocks')) {
          throw new ValidationError('NO_QUESTION_BLOCKS');
        }
        throw error;
      }
      // publish_study returns TABLE(run_token text, status text); supabase
      // gives us either an array of rows or a single row depending on the
      // PostgREST client version. Normalise to the first row.
      const row = Array.isArray(data) ? data[0] : data;
      if (!row || typeof row !== 'object') {
        throw new Error('publish_study returned no payload');
      }
      const payload = row as { run_token?: unknown; status?: unknown };
      if (typeof payload.run_token !== 'string') {
        throw new Error('publish_study returned no run_token');
      }
      return {
        run_token: payload.run_token,
        status: 'published',
      };
    },
    onSuccess: async (data, input) => {
      // Best-effort clipboard copy. Failure is silent — the toast and the
      // dialog still surface the URL so the user has a copy path.
      try {
        const url = `${window.location.origin}/r/${data.run_token}`;
        await navigator.clipboard.writeText(url);
      } catch {
        /* clipboard may be denied; toast + dialog still show the link */
      }
      toast.success('Test published. Share link copied to clipboard.');
      invalidateStudyCaches(qc, input.workspaceId, input.studyId);
    },
    onError: (err) => {
      if (err instanceof ValidationError && err.code === 'NO_QUESTION_BLOCKS') {
        toast.error('Add at least one question block before publishing.');
        return;
      }
      toast.error("Couldn't publish. Try again in a moment.");
    },
  });
}

