/**
 * Studies TanStack Query hooks — Plans 01-03 + 01-04.
 *
 * Reads:
 *   - useStudies(workspaceId)           list non-archived studies (UI: /app)
 *   - useStudiesArchived(workspaceId)   archived list (Archived tab on /app)
 *   - useStudy(studyId)                 single study lookup
 *
 * Mutations:
 *   - useCreateStudy(workspaceId)       calls create_study RPC (BUILDER-01)
 *   - useUpdateStudyTitle(studyId)      inline rename (workspace top bar)
 *   - usePublishStudy()                 publish_study RPC (D-27, TESTMGMT-02)
 *   - useMoveStudyToDraft()             move_study_to_draft RPC (TESTMGMT-02)
 *   - useArchiveStudy()                 archive_study RPC (D-28, TESTMGMT-03)
 *   - useRestoreStudy()                 restore_study RPC (D-29, TESTMGMT-03)
 *
 * Toast copy is locked to UI-SPEC.md §"Status lifecycle". Validation errors
 * (the server-side `no_question_blocks` check) surface as a destructive
 * toast with the exact copy-locked message.
 *
 * RPC typing note: the new lifecycle RPCs landed in migration 00004 and the
 * Database type in `types.gen.ts` will be regenerated by the orchestrator
 * post-merge. Until then we call `supabase.rpc(...)` with the RPC name
 * widened via `as never` (mirrors the LooseNavigate pattern from Plan 01-02),
 * structurally validating the returned shape ourselves.
 */

import { useMutation, useQuery, useQueryClient } from '@tanstack/react-query';
import { toast } from 'sonner';
import { uuidv7 } from 'uuidv7';
import { supabase } from '@/lib/supabase/auth';
import type { Database } from '@/lib/supabase/types.gen';
import { ValidationError } from '@/lib/queries/errors';

export type StudyRow = Database['public']['Tables']['studies']['Row'];

export interface UseStudiesResult {
  studies: StudyRow[];
  isLoading: boolean;
  error: Error | null;
}

function buildStudyQueryKey(workspaceId: string, archived: boolean) {
  return ['studies', workspaceId, archived ? 'archived' : 'active'] as const;
}

/**
 * Cache-invalidation helper for any mutation that changes a study's status
 * or archived_at. We invalidate both the active and archived lists for the
 * owning workspace plus the single-study cache.
 */
function invalidateStudyCaches(
  qc: ReturnType<typeof useQueryClient>,
  workspaceId: string | null | undefined,
  studyId: string,
) {
  if (workspaceId) {
    qc.invalidateQueries({ queryKey: buildStudyQueryKey(workspaceId, false) });
    qc.invalidateQueries({ queryKey: buildStudyQueryKey(workspaceId, true) });
  } else {
    qc.invalidateQueries({ queryKey: ['studies'] });
  }
  qc.invalidateQueries({ queryKey: ['study', studyId] });
}

export function useStudies(workspaceId: string | null | undefined): UseStudiesResult {
  const query = useQuery({
    queryKey: workspaceId ? buildStudyQueryKey(workspaceId, false) : ['studies', 'none', 'active'],
    enabled: !!workspaceId,
    staleTime: 10_000,
    queryFn: async (): Promise<StudyRow[]> => {
      const { data, error } = await supabase
        .from('studies')
        .select('*')
        .eq('workspace_id', workspaceId!)
        .is('archived_at', null)
        .order('updated_at', { ascending: false });
      if (error) throw error;
      return data ?? [];
    },
  });
  return {
    studies: query.data ?? [],
    isLoading: query.isLoading,
    error: (query.error as Error | null) ?? null,
  };
}

/**
 * Archived studies (`archived_at IS NOT NULL`). The Archived tab on /app
 * consumes this; the cache key is independent of the active-studies key so
 * an archive/restore mutation can invalidate both surgically.
 */
export function useStudiesArchived(workspaceId: string | null | undefined): UseStudiesResult {
  const query = useQuery({
    queryKey: workspaceId ? buildStudyQueryKey(workspaceId, true) : ['studies', 'none', 'archived'],
    enabled: !!workspaceId,
    staleTime: 30_000,
    queryFn: async (): Promise<StudyRow[]> => {
      const { data, error } = await supabase
        .from('studies')
        .select('*')
        .eq('workspace_id', workspaceId!)
        .not('archived_at', 'is', null)
        .order('archived_at', { ascending: false });
      if (error) throw error;
      return data ?? [];
    },
  });
  return {
    studies: query.data ?? [],
    isLoading: query.isLoading,
    error: (query.error as Error | null) ?? null,
  };
}

export function useStudy(studyId: string | null | undefined) {
  return useQuery({
    queryKey: ['study', studyId] as const,
    enabled: !!studyId,
    staleTime: 5_000,
    queryFn: async (): Promise<StudyRow | null> => {
      const { data, error } = await supabase
        .from('studies')
        .select('*')
        .eq('id', studyId!)
        .maybeSingle();
      if (error) throw error;
      return data;
    },
  });
}

export interface CreateStudyResult {
  studyId: string;
}

/**
 * BUILDER-01: creates a draft study + welcome + thanks atomically.
 *
 * The RPC `create_study(ws_id, study_title)` is SECURITY DEFINER and inserts
 * both pinned blocks in one transaction, returning the new study id. On
 * success we invalidate the active-studies cache so the list refreshes.
 */
export function useCreateStudy(workspaceId: string | null | undefined) {
  const qc = useQueryClient();
  return useMutation({
    mutationFn: async (input: { title?: string } = {}): Promise<CreateStudyResult> => {
      if (!workspaceId) throw new Error('Workspace not loaded');
      const { data, error } = await supabase.rpc('create_study', {
        ws_id: workspaceId,
        study_title: input.title ?? 'Untitled test',
      });
      if (error) throw error;
      if (!data) throw new Error('create_study returned no id');
      return { studyId: data };
    },
    onSuccess: () => {
      if (workspaceId) {
        qc.invalidateQueries({ queryKey: buildStudyQueryKey(workspaceId, false) });
      }
    },
  });
}

/**
 * Inline-editable test name — `<WorkspaceTopBar>` consumes this via blur/Enter.
 *
 * Optimistic update: we pre-populate `['study', studyId]` with the new title
 * before the network round-trip; if the server returns a different shape
 * (RLS strip, etc.) we reconcile from the response payload.
 */
export function useUpdateStudyTitle(studyId: string | null | undefined) {
  const qc = useQueryClient();
  return useMutation({
    mutationFn: async (input: { title: string }) => {
      if (!studyId) throw new Error('Study not loaded');
      const { data, error } = await supabase
        .from('studies')
        .update({ title: input.title })
        .eq('id', studyId)
        .select()
        .maybeSingle();
      if (error) throw error;
      return data;
    },
    onMutate: async (input) => {
      if (!studyId) return;
      await qc.cancelQueries({ queryKey: ['study', studyId] });
      const previous = qc.getQueryData<StudyRow | null>(['study', studyId]);
      if (previous) {
        qc.setQueryData<StudyRow>(['study', studyId], {
          ...previous,
          title: input.title,
        });
      }
      return { previous };
    },
    onError: (_err, _input, ctx) => {
      if (ctx?.previous) {
        qc.setQueryData(['study', studyId], ctx.previous);
      }
    },
    onSuccess: (data) => {
      if (data) {
        qc.setQueryData(['study', studyId], data);
        qc.invalidateQueries({
          queryKey: ['studies', data.workspace_id, 'active'],
        });
      }
    },
  });
}

